"""
Задание 6: Создайте список из 5 имен, а затем напишите цикл for, который:
- Выводит каждое имя в верхнем регистре.
- Напишите аналогичный цикл while.
"""

# Исходный список имен
names = ['max', 'roman', 'james', 'john', 'rosa']

# Использование цикла for для вывода имен в верхнем регистре
print("Имена в верхнем регистре (for):")
for name in names:
    print(name.upper())

# Использование цикла while для вывода имен в верхнем регистре
print("\nИмена в верхнем регистре (while):")
index = 0
while index < len(names):
    print(names[index].upper())
    index += 1
