'''
Задание 2: Создайте список из 5 чисел, найдите их сумму, произведение и среднее значение. Выведите результаты на экран.
'''

numbers = [1, 2, 3, 4, 5]

total_sum = 0  # Переименовано для избежания конфликта с встроенной функцией
prod = 1

for num in numbers:
    total_sum += num  # Суммируем числа
    prod *= num  # Умножаем числа

avg = total_sum / len(numbers)  # Находим среднее значение

print("Сумма:", total_sum)  # Выводим сумму
print("Произведение:", prod)  # Выводим произведение
print("Среднее значение:", avg)  # Выводим среднее значение
