"""
Задание 10: Напишите программу, которая запрашивает у пользователя строку и выводит:
- Длину строки
- Первую и последнюю буквы
- Список всех слов в строке (используйте метод split())
"""

# Запрос у пользователя строки
val = input("Введите строку: ")

# Длина строки
print(f"Длина строки: {len(val)}")

# Первая и последняя буквы
if val:  # Проверяем, что строка не пустая
    print(f"Первая буква: {val[0]}, Последняя буква: {val[-1]}")
else:
    print("Ошибка: строка пустая, нет первой и последней буквы.")

# Список всех слов в строке
words = val.split()
print(f"Слов в строке: {words if words else 'Нет слов.'}")

