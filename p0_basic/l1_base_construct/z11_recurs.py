"""
Задание 11: Напишите функцию factorial(n), которая вычисляет факториал числа n, используя цикл for или рекурсию. Проверьте функцию на нескольких числах.
"""


def factorial(n):
    if n < 0:
        return "Ошибка: факториал не определен для отрицательных чисел."
    elif n == 0 or n == 1:
        return 1  # Определяем 0! и 1!
    else:
        result = 1
        for i in range(2, n + 1):  # Умножаем все числа от 2 до n
            result *= i
        return result


# Проверяем функцию на нескольких числах
print(factorial(3))  # Ожидается 6 (3! = 3 * 2 * 1)
print(factorial(5))  # Ожидается 120 (5! = 5 * 4 * 3 * 2 * 1)
print(factorial(0))  # Ожидается 1 (0! = 1)
print(factorial(-1))  # Ошибка: факториал не определен для отрицательных чисел.
print(factorial(10))  # Ожидается 3628800 (10! = 10 * 9 * ... * 1)
