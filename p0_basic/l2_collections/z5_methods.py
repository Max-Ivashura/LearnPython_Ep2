# 1. Создание списка из 5 произвольных чисел
arr = [1, 5, 4, 3, 2]

# 2. Использование метода append() для добавления нового числа в конец списка
arr.append(6)

# 3. Использование метода remove() для удаления числа из списка
arr.remove(2)

# 4. Использование метода extend() для добавления другого списка из 3 чисел в текущий список
arr.extend([8, 10, 12])

# Печать финального результата
print(arr)
